package com.javaex.ex01;

public class Ex09 {

	public static void main(String[] args) {
		
		
		int a=5;
		int b=10;
		
		// 증감연산자
		System.out.println(a);
		System.out.println(++a);
		System.out.println(a);
		System.out.println(a++);
		System.out.println(a);
		
		//감소연산자
		System.out.println(b);
		System.out.println(--b);
		System.out.println(b);
		System.out.println(b--);
		System.out.println(b);
	
		System.out.println("-----------------------");
		int c=10;
		System.out.println(c-- +3);
		System.out.println(c);
	}

}
